def make_t(lang: str):
    """Returns a translation function based on the selected language: 'ar', 'tr', or 'en'."""
    if lang == "en":
        idx = 2
    elif lang == "tr":
        idx = 1
    else:
        idx = 0
        
    def t(key: str, **fmt) -> str:
        pair = TRANSLATIONS.get(key, (key, key, key))
        # Support fallback if the tuple is only length 2
        if len(pair) < 3:
            pair = (pair[0], pair[1], pair[0])
        text = pair[idx]
        if fmt:
            text = text.format(**fmt)
        return text
    return t
